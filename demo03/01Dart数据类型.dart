/**
 Dart中支持以下数据类型：

    常用数据类型：
        Numbers（数值）：
            int
            double
        String（字符串）：
            String
        Booleans（布尔）：
            bool
        List（数组）
            在Dart中，数组是列表对象，所以大多数人只是称它为列表
        Map（字典）
            通常来说，Map 是一个键值对相关的对象。键和值可以是任意类型的对象。每个键只出现一次


    项目中用不到的数据类型：
        Runes
          Rune是UTF-32编码的字符串。它可以通过文字转换成符号表情或者代表特定的数字。
        
        Symbols
          Symbol对象表示在dart程序中声明的运算符或标识符。

          在Dart中用 # 开头来表示，入门阶段不需要了解这东西，可能永远都用不到。
 */